package com.linkottut.kotlintut

fun main() {
//  String are a sequence of characters, like in an array
//  it starts with 0, 1, 2, 3... (It starts with 0, Kotlin is 0 Indexing Programming Language)
    val writer: String = "Chehov"
    println(writer[0]) // C
    println(writer[1]) // h
    println(writer.isEmpty()) // Check if value is empty
    println(writer.length) // Check the length of the String
}